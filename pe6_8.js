// 1. Set a variable 'name' and assign a function to it
let name_fun = function(arg) {
    console.log(arg);  // Output the argument to the console
};
// 2. Pass an argument into the function
name_fun("Hello, World!");  // Output: Hello, World!
// 3. Create the same function as a normal function declaration
function displayMessage(arg) {
    console.log(arg);  // Output the argument to the console
}
// Call the function with an argument
displayMessage("This is a function declaration!");  // Output: This is a function declaration!
//OUTPUT
//Hello, World!
//This is a function declaration!